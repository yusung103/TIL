package Kotlin.base

fun main(args: Array<String>) {

    val i: Int = 10
    var j: Int? = 10 // '?' null 허용

  //i = null
    j = null

    println(i)
    println(j)

    val x: String = "ABC"
    var y: String? = "ABC" // String 에도 null 가능

   //x = null
    y = null

    println(x)
    println(y)
}